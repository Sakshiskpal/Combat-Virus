        SessionFactory sf=new Configuration().configure().buildSessionFactory();
        Session session =  sf.openSession();
        Transaction tx = session.beginTransaction();
        
        String post  =(String)jComboBox1.getSelectedItem();
        if(post.equalsIgnoreCase("admin")){
        Criteria crit = session.createCriteria(Admin.class);
        crit.add(Restrictions.and(Restrictions.eq("id",userId),(Restrictions.eq("Password",pass))));
        List<UserLogin> data = crit.list();
        if(data.isEmpty())
        {
            jLabel5.setText("invalid id / password");
        }
        else{
            jLabel5.setText("");
            JOptionPane.showMessageDialog(this, "welcome");
            new AfterLogin().setVisible(true);
        }  
    }//GEN-LAST:event_jButton1ActionPerformed
